def replicarCadena():
    cadena = input('Escriba una palabra: ')
    numero = int(input(f'Escriba un numero: '))
    resul = ''
    
        
    if numero > len(cadena):
        numero == len(cadena)
        
    
    for i in cadena[:numero]:
        resul += i


    return resul * numero


