# Operadores de Asignación:

# Operador de asignación: =
print('Operador de asignación: =')

numero = 10

print('Contenido de la variable `numero`:', numero)

print()

# Operador de incremento: +=
print('Operador de incremento: +=')
numero += 90
print('Contenido de la variable `numero`:', numero)

print()

# Operador de decremento: -=

numero -= 80
print('Contenido de la variable `numero`:', numero)

print()

# Operador de asignación con producto: *=
print('Operador de asignación producto: *=')

# numero = numero * 5
numero *= 5
print('Contenido de la variable `numero`:', numero)

print()

# Operador de asignación con división: /=
print('Operador de asignación con división: /=')

# numero = numero / 25
numero /= 25
print('Contenido de la variable `numero`:', numero)

print()

numero = round(numero)

# Operador de asignación con división entera: //=
print('Operador de asignación con división entera: //=')

numero = numero // 2
print('Contenido de la variable `numero`:', numero)

print()

# Operador de asignación con resto (módulo): %=
print('Operador de asignación con resto (módulo): %=')

numero = 10

numero %= 7
print('Contenido de la variable `numero`:', numero)

print()

# Operador de asignación con and (bitwise): &=
print('Operador de asignación con and (bitwise): &=')

numero = 5

numero &= 2
print('Contenido de la variable `numero`:', numero)

print()

# Operador de asignación con or (bitwise): |=
print('Operador de asignación con or (bitwise): |=')

numero = 5

numero |= 2
print('Contenido de la variable `numero`:', numero)

print()
# Operador de asignación con xor (bitwise): ^=
print('Operador de asignación con xor (bitwise): ^=')

numero = 5

numero ^= 2
print('Contenido de la variable `numero`:', numero)

print()

# Operador de asignación con desplazamiento a la derecha (bitwise): >>=
print('Operador de asignación con desplazamiento a la derecha (bitwise): >>=')

numero = 10

numero >>= 1
print('Contenido de la variable `numero`:', numero)

print()

# Operador de asignación con desplazamiento a la izquierda (bitwise): <<=
print('Operador de asignación con desplazamiento a la izquierda (bitwise): <<=')

numero = 10

numero <<= 1
print('Contenido de la variable `numero`:', numero)
